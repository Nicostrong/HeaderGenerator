#include "functions.h"

void    ft_putchar              (char   c)
{
        write(1, &c, sizeof(char));
}

void    ft_putstr(char *str)
{
        int     i;

         i  =   0;

        while(str[i] != '\0')
        {
            ft_putchar(str[i]);
            i++;
        }
}

void    ft_strcpy              (char   *dest, char  *src)
{
        int     i;

        i   =   0;

        while(src[i] != '\0')
        {
            dest[i] =   src[i];
            i++;
        }
}

int     calcul_longueur_ligne   (char   *label)
{
        char    *p  =   label; 
        while(*p)
                p++;

        return (p - label);
}

